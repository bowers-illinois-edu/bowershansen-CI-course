## The estimator we study all day: treated mean minus control mean.
## z = treatment assignment (1 = treated, 0 = control); y = observed outcome.
diff_in_means <- function(z, y) mean(y[z == 1]) - mean(y[z == 0])

## ============================================================
## 1. Village heads: estimand, unbiasedness, and variance
##    (Gerber and Green 2012, Chapter 2)
## ============================================================

## Potential outcomes are FIXED features of the finite population.
y_c <- c(10, 15, 20, 20, 10, 15, 15)   # y_i(0): control potential outcome
y_t <- c(15, 15, 30, 15, 20, 15, 30)   # y_i(1): treated potential outcome
tau <- y_t - y_c                        # individual effects tau_i = y_i(1) - y_i(0)

N   <- length(y_c)                      # N  = 7 units
n_1 <- 2                                # treated units under complete assignment
n_0 <- N - n_1                          # control units

## Estimand: the finite-population average treatment effect (ATE).
ATE <- mean(tau)                        # = 5 percentage points

## ---- Enumerate the design ----
## Complete random assignment fixes n_1 = 2 treated of N = 7, so there are
## choose(7, 2) = 21 equally likely assignments. Build Omega: each COLUMN is
## one assignment vector z (1 = treated, 0 = control).
Omega <- apply(X = combn(x = N, m = n_1), MARGIN = 2,
               FUN = function(idx) as.integer(1:N %in% idx))
probs <- rep(x = 1 / ncol(Omega), times = ncol(Omega))   # each z equally likely

## For each assignment z, the observed outcome reveals one potential outcome:
##   Y_i = z_i * y_i(1) + (1 - z_i) * y_i(0).
## Apply the difference-in-means to each of the 21 possible data sets.
est_by_assignment <- apply(X = Omega, MARGIN = 2, FUN = function(z) {
  Y <- z * y_t + (1 - z) * y_c
  diff_in_means(z = z, y = Y)
})

## UNBIASEDNESS: averaging the 21 estimates (equal weights) returns the ATE.
sum(est_by_assignment * probs)          # = 5 = ATE

## VARIANCE: average squared distance of the estimator from the ATE.
var_diff_in_means <- sum((est_by_assignment - ATE)^2 * probs)   # ~ 21.19

## ---- Figure: the estimator's randomization distribution ----
cra_df <- data.frame(est = est_by_assignment, prob = probs)
cra_est_dist_plot <- ggplot(data = cra_df, mapping = aes(x = est, y = prob)) +
  geom_bar(stat = "identity") +
  geom_vline(xintercept = ATE, linetype = "dashed") +   # centered at the ATE
  xlab(label = "Difference-in-Means") +
  ylab(label = "Probability") +
  theme_bw()

