## ---- Variance from the (here known) potential outcomes ----
## Neyman (1923) form, population-variance convention (divide by N):
pop_var <- function(x) mean((x - mean(x))^2)
sigma2_c <- pop_var(y_c)                                  # ~ 14.29
sigma2_t <- pop_var(y_t)                                  # ~ 42.86
sigma_ct <- mean((y_c - mean(y_c)) * (y_t - mean(y_t)))   # ~  7.14
neyman_var <- (1 / (N - 1)) *
  (n_1 * sigma2_c / n_0 + n_0 * sigma2_t / n_1 + 2 * sigma_ct)   # ~ 21.19

## Imbens and Rubin (2015) form, sample-variance convention (divide by N - 1):
samp_var <- function(x) sum((x - mean(x))^2) / (length(x) - 1)
S2_t   <- samp_var(y_t)      # spread of treated potential outcomes
S2_c   <- samp_var(y_c)      # spread of control potential outcomes
S2_tau <- samp_var(tau)      # spread of individual effects (never observable)
ir_var <- S2_t / n_1 + S2_c / n_0 - S2_tau / N            # ~ 21.19

## All three variance numbers agree (they must):
c(enumerated = var_diff_in_means, neyman = neyman_var, imbens_rubin = ir_var)

## ============================================================
## 2. Consistency: stack copies of the finite population
## ============================================================
## One asymptotic sequence: replicate the 7 villages h times, so
## N = 7h units with n_1 = 2h treated. The ATE and the treated share n_1/N
## stay fixed, so the regularity conditions hold and the estimator concentrates.
##
## The estimator is DISCRETE (finite support). We get its probability mass by:
##   * exact enumeration for small N (h = 1, 2),
##   * Monte Carlo simulation for large N (h = 4, 8).

## Exact probability mass function via full enumeration of the design.
pmf_exact <- function(h) {
  yc <- rep(y_c, h); yt <- rep(y_t, h)
  Nh <- length(yc); n1h <- 2 * h
  cmb <- combn(x = Nh, m = n1h)                 # every choice of treated units
  est <- apply(X = cmb, MARGIN = 2, FUN = function(idx) {
    z <- as.integer(1:Nh %in% idx)
    diff_in_means(z = z, y = z * yt + (1 - z) * yc)
  })
  tab <- as.data.frame(table(round(est, 6)) / length(est))   # mass at each value
  data.frame(est = as.numeric(as.character(tab$Var1)), prob = tab$Freq, N = Nh)
}

## Approximate probability mass function via simulated assignments.
pmf_sim <- function(h, B = 1e5) {
  yc <- rep(y_c, h); yt <- rep(y_t, h)
  Nh <- length(yc); n1h <- 2 * h
  est <- replicate(n = B, expr = {
    z <- sample(rep(c(1, 0), c(n1h, Nh - n1h)))   # one complete random assignment
    diff_in_means(z = z, y = z * yt + (1 - z) * yc)
  })
  tab <- as.data.frame(table(round(est, 6)) / B)
  data.frame(est = as.numeric(as.character(tab$Var1)), prob = tab$Freq, N = Nh)
}

set.seed(seed = 12345)
consist_df <- rbind(pmf_exact(1), pmf_exact(2), pmf_sim(4), pmf_sim(8))
consist_df$panel <- factor(paste0("N = ", consist_df$N),
                           levels = paste0("N = ", 7 * c(1, 2, 4, 8)))

## ---- Figure: discrete distribution tightens on the ATE as N grows ----
## Same x-axis across panels (so the concentration is visible), but FREE y-axes
## (heights stay comparable). No y-axis label: the point is just that the
## estimator concentrates around the ATE.
consistency_sim_plot <- ggplot(data = consist_df, mapping = aes(x = est, y = prob)) +
  geom_segment(aes(xend = est, yend = 0), colour = "grey45", linewidth = 0.35) +
  geom_point(colour = "grey25", size = 0.45) +
